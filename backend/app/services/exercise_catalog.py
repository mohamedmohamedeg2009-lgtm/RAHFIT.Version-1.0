def _exercise(
    id: str,
    name: str,
    description: str,
    muscles: tuple[MuscleGroup, ...],
    equipment: tuple[Equipment, ...],
    difficulty: ExerciseDifficulty,
    movement: MovementType,
    exercise_type: ExerciseType,
    classification: ExerciseClassification,
    reps: str,
    sets: tuple[int, int],
    rest: tuple[int, int],
    tempo: str,
    *,
    contraindications: tuple[str, ...] = (),
    supported_injuries: tuple[str, ...] = (),
    tags: tuple[str, ...] = (),
) -> Exercise:
    return Exercise(
        id=id,
        name=name,
        description=description,
        muscle_groups=muscles,
        equipment=equipment,
        difficulty=difficulty,
        experience_level=ExperienceLevel(difficulty.value),
        movement_type=movement,
        exercise_type=exercise_type,
        classification=classification,
        recommended_reps=reps,
        recommended_sets=sets,
        rest_range_seconds=rest,
        tempo=tempo,
        contraindications=contraindications,
        supported_injuries=supported_injuries,
        tags=tags,
    )


