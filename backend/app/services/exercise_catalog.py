@dataclass(frozen=True)
class ExerciseCatalog:
    exercises: tuple[Exercise, ...] = EXERCISES
    version: int = 1

    def active(self) -> tuple[Exercise, ...]:
        return tuple(
            item for item in self.exercises if item.active and item.version == self.version
        )

    def get(self, exercise_id: str) -> Exercise | None:
        return next((item for item in self.active() if item.id == exercise_id), None)
