CATALOG_VERSION = 1
ALL = (ExerciseLocation.HOME, ExerciseLocation.GYM, ExerciseLocation.OUTDOOR)
HOME_GYM = (ExerciseLocation.HOME, ExerciseLocation.GYM)


def _exercise(
    id: str,
    name: str,
    category: ExerciseCategory,
    pattern: MovementPattern,
    muscles: tuple[MuscleGroup, ...],
    equipment: tuple[Equipment, ...],
    locations: tuple[ExerciseLocation, ...],
    *,
    difficulty: ExperienceLevel = ExperienceLevel.BEGINNER,
    exclusions: tuple[str, ...] = (),
    mobility: tuple[str, ...] = (),
    contraindications: tuple[str, ...] = (),
    pain_exclusions: tuple[str, ...] | None = None,
    chronic_exclusions: tuple[str, ...] | None = None,
    clearance_required: bool = False,
    requires_all_equipment: bool = False,
    reps: tuple[int, int] = (8, 12),
    sets: int = 3,
    rest: int = 60,
    minutes: int = 5,
) -> CatalogExercise:
    return CatalogExercise(
        id=id,
        name=name,
        category=category,
        movement_pattern=pattern,
        primary_muscles=muscles,
        equipment=equipment,
        requires_all_equipment=requires_all_equipment,
        suitable_locations=locations,
        difficulty=difficulty,
        injury_exclusions=exclusions,
        pain_area_exclusions=pain_exclusions if pain_exclusions is not None else exclusions,
        mobility_requirements=mobility,
        contraindications=contraindications,
        chronic_condition_exclusions=(
            chronic_exclusions if chronic_exclusions is not None else contraindications
        ),
        requires_medical_clearance=clearance_required,
        default_rep_range=reps,
        default_sets=sets,
        default_rest_seconds=rest,
        estimated_duration_minutes=minutes,
        instructions=("Use controlled range of motion and stable posture.",),
        safety_notes=("Stop if pain, dizziness, or unusual symptoms occur.",),
        version=CATALOG_VERSION,
    )


