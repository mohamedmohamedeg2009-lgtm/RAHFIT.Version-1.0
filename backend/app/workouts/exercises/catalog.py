class ExerciseCatalog:
    def __init__(self, exercises: tuple[CatalogExercise, ...] = _EXERCISES) -> None:
        ids = tuple(item.id for item in exercises)
        if len(ids) != len(set(ids)):
            raise ValueError("Exercise catalog IDs must be unique.")
        patterns = {item.movement_pattern for item in exercises}
        missing = set(MovementPattern) - patterns
        if missing:
            raise ValueError(
                "Exercise catalog is missing movement patterns: "
                + ",".join(sorted(str(item) for item in missing))
            )
        self._exercises = tuple(item for item in exercises if item.active)

    @property
    def version(self) -> int:
        return CATALOG_VERSION

    def all(self) -> tuple[CatalogExercise, ...]:
        return self._exercises

    def get(self, exercise_id: str) -> CatalogExercise | None:
        return next((item for item in self._exercises if item.id == exercise_id), None)
