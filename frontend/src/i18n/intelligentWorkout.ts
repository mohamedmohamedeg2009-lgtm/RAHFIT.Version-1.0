export function workoutText(
  locale: Locale,
  key: IntelligentWorkoutKey,
  values: Record<string, string | number> = {},
): string {
  return Object.entries(values).reduce(
    (message, [name, value]) => message.replaceAll(`{${name}}`, String(value)),
    intelligentWorkoutCopy[locale][key],
  );
}

export function workoutEnumLabel(value: string, locale: Locale): string {
  return enumLabels[value]?.[locale] ?? value.replaceAll("_", " ");
}

export function formatWorkoutDate(value: string, locale: Locale): string {
  return new Intl.DateTimeFormat(locale === "ar" ? "ar" : "en", {
    dateStyle: "medium",
    timeStyle: "short",
  }).format(new Date(value));
}
